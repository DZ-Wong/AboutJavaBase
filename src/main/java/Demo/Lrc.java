    /**
     * 计算LRC
     *
     * @Title: LRC
     * @Description: TODO
     * @param @param bytes
     * @param @param length
     * @param @return
     * @return int
     * @throws
     */
    public static int LRC(int[] bytes) {
        int checksum = 0;
        for (int i = 0; i < bytes.length; i++) {
            checksum ^= (bytes[i] & 0xFF);
        }
        return checksum;
    }


